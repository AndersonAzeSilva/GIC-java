    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // As linhas abaxio substituem a label data lblData pela data atual do 
        // sistema ao iniciar o formulário
        Date data = new Date();
        SimpleDateFormat formatador = new SimpleDateFormat("dd/MM/yyyy");
        lblData.setText(formatador.format(data)); //Formatando apenas a Data atual
        // Criando um Timer para atualizar a hora a cada segundo
        Timer timer = new Timer(1000, new ActionListener() {

            public void actionPerformed(ActionEvent e) {
                Date horaAtual = new Date();
                SimpleDateFormat formatadorHora = new SimpleDateFormat("HH:mm:ss"); // Formato de 24h
                lblHora.setText("Hora: " + formatadorHora.format(horaAtual));
            }
        });
        timer.start(); // Inicia o Timer
    }//GEN-LAST:event_formWindowActivated

