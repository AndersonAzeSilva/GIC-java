    private void MenuRelatorioChamadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MenuRelatorioChamadoActionPerformed
        // criando a instrução para gerar o relátorio de chamados
        int confirma = JOptionPane.showConfirmDialog(null, "Você confirma a emissão do "
                + "relatório de chamados? ", "Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            // emitindo relário com o framework JasperReports
            try {
                //Usando a classe JasperPrint para preprar a impressão de um relatório
                JasperPrint print = JasperFillManager.fillReport(getClass().getResourceAsStream("/reports/chamados.jasper"), null, conexao);
                // a linha a seguir exibi o relat´rio através da classe JasperViwer
                JasperViewer.viewReport(print, false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_MenuRelatorioChamadoActionPerformed

